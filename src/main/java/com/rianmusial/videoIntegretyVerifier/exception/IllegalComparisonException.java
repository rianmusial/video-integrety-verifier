package com.rianmusial.videoIntegretyVerifier.exception;

public class IllegalComparisonException extends IllegalArgumentException {

	public IllegalComparisonException() {
		super();
	}

	public IllegalComparisonException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalComparisonException(String s) {
		super(s);
	}

	public IllegalComparisonException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -6355013901353824372L;

}
